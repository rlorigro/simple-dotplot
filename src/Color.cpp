string Viridis::get_svg_color(double x){
    auto rgb = get_rgb(x);
    string r = to_string(uint16_t(round(rgb[0]*255)));
    string g = to_string(uint16_t(round(rgb[1]*255)));
    string b = to_string(uint16_t(round(rgb[2]*255)));
    string s = "rgb(" + r + ',' + g + ',' + b + ")";
    return s;
}
